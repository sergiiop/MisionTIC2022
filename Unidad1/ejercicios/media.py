def run():
  valor_1 = int(input('Ingrese el primer numero: '))
  valor_2 = int(input('Ingrese el segundo numero: '))
  valor_3 = int(input('Ingrese el tercer numero: '))
  media = (valor_1 + valor_2 + valor_3)/3
  
  print(f'la media aritmetica de los numeros {valor_1}, {valor_2}, {valor_3} es = {media}')
  

if __name__ == "__main__":
    run()
